#include <stdio.h>
void main() {
    int count = 1;
    for (int i = 1; i <= 3; i++) {
        for (int j = 3; j >= i; j--) {
            printf("%d ", j);
            count++; // 1 2 3 4 5 6 7 8 ... 
        }
        printf("\n");
        count = 1;
    }
}

